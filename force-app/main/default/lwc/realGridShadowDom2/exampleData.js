export async function setDefaultDataform(dataList, firstYear, cnt){
    const newDataList = dataList.map(data => {
        for (let year = firstYear; year < (firstYear+cnt); year++) {
            for (let month = 1; month <= 12; month++) {
                const monthKey = `${year}_${month}_Amt`;
                const qtyKey = `${year}_${month}_Qty`;
                const priceKey = `${year}_${month}_Price`;

                if (!data[monthKey]) {
                    data[monthKey] = "0";
                }
                if (!data[qtyKey]) {
                    data[qtyKey] = "0";
                }
                if (!data[priceKey]) {
                    data[priceKey] = "0"; 
                }
            }
        }
        return newDataList;
    })
}

